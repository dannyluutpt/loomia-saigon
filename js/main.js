/* ==========================================
   TRANSLATION ENGINE
   ========================================== */
function switchLanguage(lang) {
    if (!translations[lang]) return;
    currentLanguage = lang;
    localStorage.setItem('preferredLanguage', lang);
    
    // Toggle active state of language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.textContent.trim().toLowerCase() === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });

    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (translations[lang][key].includes('<') || translations[lang][key].includes('&')) {
                element.innerHTML = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });

    // Translate all placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) {
            element.setAttribute('placeholder', translations[lang][key]);
        }
    });

    // Translate document title
    const currentFile = window.location.pathname.split('/').pop() || 'index.html';
    let titleKey = 'title_home';
    if (currentFile === 'about.html') titleKey = 'title_about';
    else if (currentFile === 'services.html') titleKey = 'title_services';
    else if (currentFile === 'collection.html') titleKey = 'title_products';
    else if (currentFile === 'configurator.html') titleKey = 'title_config';
    else if (currentFile === 'contact.html') titleKey = 'title_contact';
    
    if (translations[lang][titleKey]) {
        document.title = translations[lang][titleKey];
    }

    // Re-run calculator if we are on the configurator page to translate labels
    if (document.getElementById('fabricLength')) {
        updateCalculator();
    }
}

