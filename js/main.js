// Update calculated output
function updateCalculator() {
    const slider = document.getElementById('fabricLength');
    if (!slider) return; // Null safety if not on configurator page
    
    configState.length = parseInt(slider.value);
    
    // Format display text
    const lengthDisplay = document.getElementById('lengthDisplay');
    if (lengthDisplay) {
        lengthDisplay.textContent = configState.length.toLocaleString('en-US') + 'm';
    }

    // Base price math
    let pricePerMeter = configState.weavePrice * configState.fiberMultiplier;
    let rawTotal = pricePerMeter * configState.length;

    // Bulk volume discount
    let discount = 0;
    if (configState.length >= 5000) {
        discount = 0.10;
    } else if (configState.length >= 2000) {
        discount = 0.05;
    }
    
    let discountedTotal = rawTotal * (1 - discount);
    discountedTotal = Math.round(discountedTotal / 100000) * 100000;

    // Update cost text
    const costDisplay = document.getElementById('totalCostDisplay');
    if (costDisplay) {
        costDisplay.textContent = discountedTotal.toLocaleString('vi-VN') + ' VNĐ';
    }

    // Lead time logic
    let leadTimeDays = 8 + Math.ceil(configState.length / 1000) * 2;
    if (configState.weave === 'jacquard') leadTimeDays += 3;
    if (configState.fiber === 'silk') leadTimeDays += 2;
    
    const lang = currentLanguage;
    const isEn = lang === 'en';

    let weaveText = isEn ? configState.weaveLabel.split(' / ')[1] : configState.weaveLabel.split(' / ')[0];
    let fiberText = isEn ? translations.en['f_' + configState.fiber] : translations.vi['f_' + configState.fiber];
    
    let colorText = configState.colorLabel;
    if (isEn) {
        if (colorText.includes('Đỏ Rượu Vang')) colorText = 'Burgundy Red (Fire Aspect)';
        else if (colorText.includes('Xanh Lục Bảo')) colorText = 'Emerald Green (Wood Aspect - Auspicious)';
        else if (colorText.includes('Vàng Champagne')) colorText = 'Champagne Gold (Metal)';
        else if (colorText.includes('Nâu Đất')) colorText = 'Warm Brown (Earth)';
        else if (colorText.includes('Tím Mận')) colorText = 'Plum Purple (Fire)';
        else if (colorText.includes('Kem Sợi')) colorText = 'Natural Linen Cream';
    }

    // Write to DOM summary
    const summaryWeave = document.getElementById('summaryWeave');
    const summaryFiber = document.getElementById('summaryFiber');
    const summaryColor = document.getElementById('summaryColor');
    const summaryLeadtime = document.getElementById('summaryLeadtime');
    const previewFabricName = document.getElementById('previewFabricName');

    if (summaryWeave) summaryWeave.textContent = weaveText || configState.weaveLabel;
    if (summaryFiber) summaryFiber.textContent = fiberText || configState.fiberLabel;
    if (summaryColor) summaryColor.textContent = colorText;
    if (summaryLeadtime) summaryLeadtime.textContent = leadTimeDays + (isEn ? " Days" : " ngày");

    if (previewFabricName) {
        let previewName = (isEn ? translations.en['f_' + configState.fiber] : translations.vi['f_' + configState.fiber]) + ' ' + 
                         (isEn ? translations.en['w_' + configState.weave] : translations.vi['w_' + configState.weave]);
        previewFabricName.textContent = previewName;
    }
}

